<template>
    <div class="_404">
    <h2 class="m-0">抱歉，页面未找到，<span>{{countDown}}</span>s后自动跳转到
        <a href="javascript:;" @click="goHome">回到上一页</a>
    </h2>
    <img src="../assets/404.png" alt="页面未找到">
    </div>

</template>
<script>
    export default {
        name: "NF404",
        data() {
            return {
                countDown: 5,
                timer: null
            }
        },
        methods: {
            goHome() {
                history.go(-1)
                clearInterval(this.timer)
            }
        },
        created() {
            setInterval(() => {
                this.countDown > 0 ? this.countDown-- : this.goHome()
            }, 1000)
        }
    }
</script>


